package edu.rutgers.dimacs.reu.utility;

public class HierarchyTreeException extends Exception {

	private static final long serialVersionUID = -66075490164497605L;

	public HierarchyTreeException() {
	}

	public HierarchyTreeException(String arg0) {
		super(arg0);
	}

	public HierarchyTreeException(Throwable arg0) {
		super(arg0);
	}

	public HierarchyTreeException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public HierarchyTreeException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

}
